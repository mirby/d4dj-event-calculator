function generateCardArray() {

    cardArray = [];

    var tempEt = new Map();
    var tempParam = new Map();
    var storage = window.localStorage;
    var obj = JSON.parse(storage.getItem("et"));
    if (obj) {
        tempEt = new Map(obj);
    }

    obj = JSON.parse(storage.getItem("param"));
    if (obj) {
        
        obj.forEach(function(x) {
            for (var key in x) {
                tempParam.set(key, x[key]);
            }
        });
    }

    Object.keys(cards).forEach(function(key) {

        var heartMod = 0;
        var techMod = 0;
        var physMod = 0;

        var heart = cards[key].heart;
        var technical = cards[key].technical;
        var physical = cards[key].physical;

        var et = tempEt.get(cards[key].id.toString());
        var etVal = 0;

        switch (et) {
            case "1":
                etVal = 2000;
                break;
            case "2":
                etVal = 2500;
                break;
            case "3":
                etVal = 3000;
                break;
            case "4":
                etVal = 4500;
                break;
            default:
                etVal = 0;
        }

        if (heart > technical) {
            if (heart > physical) {
                heartMod += etVal;
            } else {
                physMod += etVal;
            }
        } else if (technical > physical) {
            techMod += etVal;
        } else {
            physMod += etVal;
        }

        var heartParamPerc = parseFloat(tempParam.get("param_" + cards[key].character.toLowerCase() + "_1")) / 100;
        if (isNaN(heartParamPerc)) {
            heartParamPerc = 0;
        }
        heartMod += Math.floor((heart + heartMod) * heartParamPerc);

        var techParamPerc = parseFloat(tempParam.get("param_" + cards[key].character.toLowerCase() + "_2")) / 100;
        if (isNaN(techParamPerc)) {
            heartParamPerc = 0;
        }
        techMod += Math.floor((technical + techMod) * techParamPerc);

        var physParamPerc = parseFloat(tempParam.get("param_" + cards[key].character.toLowerCase() + "_3")) / 100;
        if (isNaN(physParamPerc)) {
            heartParamPerc = 0;
        }
        physMod += Math.floor((physical + physMod) * physParamPerc);

        cardArray.push({
            "id":cards[key].id,
            "character":cards[key].character,
            "cardname":cards[key].cardname,
            "unit":cards[key].unit,
            "type":cards[key].type,
            "rarity":cards[key].rarity,
            "heart":parseInt(heart + heartMod),
            "technical":parseInt(technical + techMod),
            "physical":parseInt(physical + physMod),
            "totalpower":parseInt(heart + heartMod + technical + techMod + physical + physMod),
            "skill":cards[key].skill,
            "icon":cards[key].icon,
            "passive":cards[key].passive,
        })
    });

    standardArray = [...cardArray];
}
