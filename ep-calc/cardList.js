var cardArray = [];
var cardArray2 = [];
var standardArray = [];

function generateCardArray() {

    cardArray = [];

    var tempStats = new Map();
    var tempEt = new Map();
    var tempParam = new Map();
    var storage = window.localStorage;

    var obj = JSON.parse(storage.getItem("growth"));
    if (obj) {
        tempStats = new Map(obj);
    }

    obj = JSON.parse(storage.getItem("et"));
    if (obj) {
        tempEt = new Map(obj);
    }

    obj = JSON.parse(storage.getItem("param"));
    if (obj) {
        obj.forEach(function(x) {
            tempParam.set(x[0], x[1]);
        });
    }

    Object.keys(cards).forEach(function(key) {

        var heartMod = 0;
        var techMod = 0;
        var physMod = 0;

        var heart = cards[key].heart;
        var tempHeart = tempStats.get(cards[key].id.toString() + "_heart");
        if (tempHeart) {
            heart = parseInt(tempHeart);
        }
        var technical = cards[key].technical;
        var tempTech = tempStats.get(cards[key].id.toString() + "_tech");
        if (tempTech) {
            technical = parseInt(tempTech);
        }
        var physical = cards[key].physical;
        var tempPhys = tempStats.get(cards[key].id.toString() + "_phys");
        if (tempPhys) {
            physical = parseInt(tempPhys);
        }
        var skill = cards[key].skill;
        var tempSkill = tempStats.get(cards[key].id.toString() + "_skill");
        if (tempSkill) {
            skill = parseFloat(tempSkill);
        }

        var et = tempEt.get(cards[key].id.toString());
        var etVal = 0;

        switch (et) {
            case "1":
                etVal = 2000;
                break;
            case "2":
                etVal = 2500;
                break;
            case "3":
                etVal = 3000;
                break;
            case "4":
                etVal = 4500;
                break;
            default:
                etVal = 0;
        }

        if (heart > technical) {
            if (heart > physical) {
                heartMod += etVal;
            } else {
                physMod += etVal;
            }
        } else if (technical > physical) {
            techMod += etVal;
        } else {
            physMod += etVal;
        }

        var heartParamPerc = parseFloat(tempParam.get("param_" + cards[key].character.toLowerCase() + "_1")) / 100;
        if (isNaN(heartParamPerc)) {
            heartParamPerc = 0;
        }
        heartMod += Math.floor((heart + heartMod) * heartParamPerc);

        var techParamPerc = parseFloat(tempParam.get("param_" + cards[key].character.toLowerCase() + "_2")) / 100;
        if (isNaN(techParamPerc)) {
            techParamPerc = 0;
        }
        techMod += Math.floor((technical + techMod) * techParamPerc);

        var physParamPerc = parseFloat(tempParam.get("param_" + cards[key].character.toLowerCase() + "_3")) / 100;
        if (isNaN(physParamPerc)) {
            physParamPerc = 0;
        }
        physMod += Math.floor((physical + physMod) * physParamPerc);

        cardArray.push({
            "id":cards[key].id,
            "character":cards[key].character,
            "cardname":cards[key].cardname,
            "unit":cards[key].unit,
            "type":cards[key].type,
            "rarity":cards[key].rarity,
            "heart":parseInt(heart + heartMod),
            "technical":parseInt(technical + techMod),
            "physical":parseInt(physical + physMod),
            "totalpower":parseInt(heart + heartMod + technical + techMod + physical + physMod),
            "skill":skill,
            "icon":cards[key].icon,
            "passive":cards[key].passive,
        })

        if (cards[key].growth != null) {
            cardArray2.push({
                "id":cards[key].id,
                "character":cards[key].character,
                "cardname":cards[key].cardname,
                "unit":cards[key].unit,
                "type":cards[key].type,
                "rarity":cards[key].rarity,
                "heart":parseInt(heart + heartMod),
                "technical":parseInt(technical + techMod),
                "physical":parseInt(physical + physMod),
                "totalpower":parseInt(heart + heartMod + technical + techMod + physical + physMod),
                "skill":skill,
                "icon":cards[key].icon,
                "passive":cards[key].passive,
            })
        }
    });

    standardArray = [...cardArray];
}
